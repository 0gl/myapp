class Candidato {
    constructor(nome, mae, pai, curso, endereco, cep, cidade, estado) {
        this.nome = nome;
        this.mae = mae;
        this.pai = pai;
        this.curso = curso;
        this.endereco = endereco;
        this.cep = cep;
        this.cidade = cidade;
        this.estado = estado;
    }
}

module.exports = Candidato;
